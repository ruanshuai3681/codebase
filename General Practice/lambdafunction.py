numbers = [1, 2, 3, 4, 5]
squared_numbers = map(lambda x: x ** 2, numbers)
squared_numbers_list = list(squared_numbers)
print(squared_numbers_list)  # Output: [1, 4, 9, 16, 25]
#In Python, a lambda function is a small anonymous function defined using the lambda keyword. It can take any number of
# arguments, but can only have one expression. Lambda functions are often used when you need a simple function for
# a short period of time and don't want to define a separate function using the def keyword.
